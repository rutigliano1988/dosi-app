const STRINGS: Record<Lang, StringsMap> = { es, en };

export function tstr(lang: Lang, key: string, vars: Record<string, string | number> = {}): string {
  const raw = (STRINGS[lang] ?? STRINGS.es)[key] ?? key;
  if (typeof raw !== 'string') return key;
  return raw.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}

export function tArr(lang: Lang, key: string): string[] {
  const raw = (STRINGS[lang] ?? STRINGS.es)[key];
  if (typeof raw === 'string') {
    try { return JSON.parse(raw) as string[]; } catch { return []; }
  }
  return Array.isArray(raw) ? raw : [];
}
